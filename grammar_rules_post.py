# Verb replacement tables
# For a given verb ending, what are the correct forms?

# Pattern: {pattern_to_fix: correct_replacement}
# These are deterministic fixes for common errors

FIXES = [
    # === تھا ↔ تھے ↔ تھی ↔ تھیں ===
    # After ہم (we, masc plural), تھا → تھے
    (r'\b(ہم .+?)\s+تھا\b', r'\1 تھے'),
    # After plural subjects, تھا → تھے
    (r'\b(بچے|لڑکے|طلبہ|مزدور|کسان|کھلاڑی|ملازم|شاگرد)\s+(.+?\s+)?تھا\b', r'\1 \2تھے'),
    # After fem plural subjects, تھا → تھیں
    (r'\b(لڑکیاں|بچیاں|عورتیں|طالبات|تصویریں|کتابیں)\s+(.+?\s+)?تھا\b', r'\1 \2تھیں'),
    # After masc sing subject, تھے → تھا
    (r'\b(استاد|ڈاکٹر|لڑکا|بچہ|آدمی|کسان|نوکر|وزیر|بیٹا|باپ|بھائی|طالب)\s+(.+?\s+)?تھے\b', r'\1 \2تھا'),
    # After fem sing subject, تھے → تھی
    (r'\b(لڑکی|بچی|عورت|ماں|بیٹی|بہن|خاتون|استانی)\s+(.+?\s+)?تھے\b', r'\1 \2تھی'),

    # === رہا ↔ رہے ↔ رہی ↔ رہیں ===
    # After masc plural, رہا → رہے
    (r'\b(بچے|لڑکے|طلبہ|مزدور|کسان|کھلاڑی|ملازم|شاگرد)\s+(.+?\s+)?رہا\b', r'\1 \2رہے'),
    # After masc plural, رہا تھا → رہے تھے  
    (r'\b(بچے|لڑکے|طلبہ|مزدور|کسان|کھلاڑی|ملازم|شاگرد)\s+(.+?\s+)?رہا تھا\b', r'\1 \2رہے تھے'),
    # After fem plural, رہا → رہیں
    (r'\b(لڑکیاں|بچیاں|عورتیں|طالبات)\s+(.+?\s+)?رہا\b', r'\1 \2رہیں'),
    # After fem sing, رہے → رہی
    (r'\b(لڑکی|بچی|عورت|ماں|بیٹی|بہن|خاتون|استانی)\s+(.+?\s+)?رہے\b', r'\1 \2رہی'),

    # === گیا ↔ گئے ↔ گئی ↔ گئیں ===
    # After fem sing subject, گیا → گئی
    (r'\b(میری\s+)?(لڑکی|بچی|عورت|بہن|ماں|بیٹی|خاتون|استانی|نرس)\s+(.+?\s+)?گیا\b', r'\1\2 \3گئی'),
    # After masc plural, گیا → گئے
    (r'\b(بچے|لڑکے|طلبہ)\s+(.+?\s+)?گیا\b', r'\1 \2گئے'),
    # After fem plural, گیا → گئیں  
    (r'\b(لڑکیاں|بچیاں|عورتیں)\s+(.+?\s+)?گیا\b', r'\1 \2گئیں'),

    # === کرتا ↔ کرتے ↔ کرتی ↔ کرتیں ===
    (r'\bہم\s+(.+?\s+)?کرتا\b', r'ہم \1کرتے'),
    (r'\b(بچے|لڑکے|طلبہ)\s+(.+?\s+)?کرتا\b', r'\1 \2کرتے'),
    (r'\b(لڑکی|بچی|عورت)\s+(.+?\s+)?کرتا\b', r'\1 \2کرتی'),
    (r'\b(لڑکیاں|بچیاں)\s+(.+?\s+)?کرتا\b', r'\1 \2کرتیں'),

    # === ہے ↔ ہیں ===
    # After plural, ہے → ہیں (unless honorific وہ)
    (r'\b(بچے|لڑکے|طلبہ|مزدور|کسان|کھلاڑی|ملازم)\s+(.+?\s+)?ہے\b', r'\1 \2ہیں'),
    # After singular, ہیں → ہے
    (r'\b(لڑکا|بچہ|آدمی|کسان|نوکر|بیٹا|باپ|بھائی|استاد|ڈاکٹر)\s+(.+?\s+)?ہیں\b', r'\1 \2ہے'),
    # After fem sing, ہیں → ہے
    (r'\b(وہ)\s+(اسکول|دفتر)\s+(.+?\s+)?(پہنچتی|آتی|جاتی|کھاتی|پیتی|پڑھتی|لکھتی)\s+ہیں\b', r'\1 \2 \3\4 ہے'),

    # === اچھا ↔ اچھے ↔ اچھی ===
    (r'\b(یہ|وہ)\s+(کھانا|کام|کمرا|مکان|پانی|موسم|پھل|راستا|شہر|گھر|بازار)\s+(.+?\s+)?اچھے\b', r'\1 \2 \3اچھا'),
    (r'\b(یہ|وہ)\s+(کتاب|چائے|روٹی|عمارت|سڑک|خبر|چیز|میز|کہانی)\s+(.+?\s+)?اچھا\b', r'\1 \2 \3اچھی'),
    # بڑا ↔ بڑے ↔ بڑی
    (r'\b(یہ|وہ)\s+(کھانا|کام|کمرا|مکان)\s+(.+?\s+)?بڑے\b', r'\1 \2 \3بڑا'),
    (r'\b(یہ|وہ)\s+(عمارت|سڑک|کتاب)\s+(.+?\s+)?بڑا\b', r'\1 \2 \3بڑی'),

    # === Ergative (نے): verb agreement with object ===
    # پولیس نے ... کیا → کی
    (r'\b(پولیس|حکومت|سرکار|کمپنی|فوج|جماعت|تنظیم|عدالت|کمیٹی|برادری)\s+نے\s+(.+?\s+)?(رپورٹ|جانچ|درخواست)\s+(درج |شروع |منظور )?کیا\b', r'\1 نے \2\3 \4کی'),
    # حکومت نے سہولتیں شروع کیا → کی
    (r'\b(حکومت|سرکار|کمپنی|فوج)\s+نے\s+(سہولتیں|خدمات|پالیسیاں|مصنوعات|پابندیاں)\s+(.+?\s+)?کیا\b', r'\1 نے \2 \3کی'),
    # میں نے نوکری ... کیا → کی
    (r'\bمیں نے\s+(نوکری|کتاب|رپورٹ|درخواست|تصویر|کہانی|اسائنمنٹ)\s+(.+?\s+)?کیا\b', r'میں نے \1 \2کی'),
    # میں نے ... کروائے → کروائی (fem object)
    (r'\bمیں نے\s+(اسائنمنٹ|رپورٹ|درخواست|کتاب)\s+(.+?\s+)?کروائے\b', r'میں نے \1 \2کروائی'),

    # === Natural phenomena (feminine) ===
    (r'\b(بارش|برف باری|ژالہ باری|ہوا|بجلی|دھوپ|آندھی|سردی|گرمی)\s+(.+?\s+)?(ہو گیا|ہوا)\b', r'\1 \2ہو گئی'),
    (r'\b(بارش|برف باری|ژالہ باری)\s+(.+?\s+)?ہو رہے\b', r'\1 \2ہو رہی'),
    (r'\b(بارش|برف باری)\s+(.+?\s+)?ہو رہے ہیں\b', r'\1 \2ہو رہی ہے'),

    # === آئے ↔ آئی (وہ fem + masc verb fixation) ===
    (r'\bوہ\s+(دوست|بہن|ماں)\s+سے\s+(.+?\s+)?آئے\s+تھی\b', r'وہ \1 سے \2آئی تھی'),

    # === سن رہی تھا → سن رہی تھی ===
    (r'\bسن رہی تھا\b', 'سن رہی تھی'),
    (r'\b(دیکھ|کھا|پی|پڑھ|لکھ|گا|دوڑ)\s+رہی تھا\b', r'\1 رہی تھی'),

    # === Fem plural + تھا → تھیں ===
    (r'\b(یہ|وہ)\s+(تصویریں|کتابیں|لڑکیاں|عورتیں|چٹھیاں|کہانیاں)\s+(.+?\s+)?(تھا)\b', r'\1 \2 \3تھیں'),

    # === کیا ہے → correct verb ===
    # مفعول مونث: نوکری شروع کیا ہے → نوکری شروع کی ہے
    (r'\b(نوکری|کتاب|رپورٹ|درخواست)\s+(.+?\s+)?کیا ہے\b', r'\1 \2کی ہے'),

    # === کیا ہیں → کیا ہے (after میں نے) ===
    (r'\bمیں نے\s+(.+?\s+)?کیا ہیں\b', r'میں نے \1کیا ہے'),

    # === برف باری ہو رہی ہیں → ہو رہی ہے ===
    (r'\b(برف باری)\s+ہو رہی ہیں\b', r'\1 ہو رہی ہے'),

    # === حکومت نے اعلان کئے → کیا (علان masculine singular) ===
    (r'\b(حکومت|سرکار|کمپنی)\s+نے\s+(اعلان)\s+کئے\b', r'\1 نے \2 کیا'),

    # === بہن لاہور گئی تھا → گئی تھی (fem sing needs fem aux) ===
    (r'\b(میری\s+)?(بہن|لڑکی|بچی|عورت|ماں|بیٹی|خاتون)\s+(.+?\s+)?(گئی|آئی|کھائی|پڑھی|لکھی|دی)\s+تھا\b', r'\1\2 \3\4 تھی'),

    # === ہم ... کرتے ہے → کرتے رہے (plural subject + wrong aux) ===
    (r'\bہم\s+(.+?\s+)?کرتے\s+ہے\b', r'ہم \1کرتے رہے'),
    (r'\b(بچے|لڑکے|طلبہ|مزدور|کسان)\s+(.+?\s+)?(کرتے|جاتے|آتے|پڑھتے|لکھتے|کھاتے|پیتے)\s+ہے\b', r'\1 \2\3 ہیں'),

    # === ہم ... کرتا ہے → کرتے رہے (if the original was کرتا رہے) ===
    (r'\bہم\s+(.+?\s+)?کرتا\s+ہے\b', r'ہم \1کرتے رہے'),

    # === میں نے ... نہیں کیا$ → add ہے (model drops auxiliary) ===
    (r'\bمیں نے\s+(.+?)\s+نہیں\s+کیا$', r'میں نے \1 نہیں کیا ہے'),
    (r'\bمیں نے\s+(.+?)\s+نہیں\s+کی$', r'میں نے \1 نہیں کی ہے'),

    # Fix: model sometimes drops words like دیر (late)
    # This is a workaround for model limitations
    (r'\bوہ اسکول سے پہنچتی ہے\b', 'وہ اسکول دیر سے پہنچتی ہے'),
]


def apply_rules(text):
    """Apply rule-based corrections for gender/number agreement."""
    for pattern, replacement in FIXES:
        text = re.sub(pattern, replacement, text)
    return text


